#include <iostream>

using namespace std;

int main() {
   float PI = 3.14; // Constant declaration

   cout << (int)PI << endl; // Typecasting float to int

   cout << ((float)10/3) << endl; // Typecasting int to float

   cout << ((char)('A' + 1)) << endl; // Typecasting char to char

    return 0;
}